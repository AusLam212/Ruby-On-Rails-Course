# No new line after print (\n creates new line)
print "Hello World\n"

# Creates new line after printing
puts "Hello World"

# Returns argument from what it prints
p "Hello World"

# Using variable to puts "Hello World"
greeting = "Hello World"

puts greeting

# Using a method to print "Hello World"
def greet(thing_to_say)
    puts thing_to_say
end

greet("Hello World")